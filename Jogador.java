class Jogador {
    String nome;
    String jogo;
    String genero;

    public Jogador(String nome, String jogo, String genero) {
        this.nome = nome;
        this.jogo = jogo;
        this.genero = genero;
    }

    public String getNome() {
        return nome;
    }

    public String getJogo() {
        return jogo;
    }

    public String getGenero() {
        return genero;
    }
}


